mood = "happy"

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Talk to me.")
elif mood == "excited":
    print("Yeaaahh!")
elif mood == "relaxed":
    print ("You are calm and not worried or tense.")
else:
    print("I don't recognize this mood.")